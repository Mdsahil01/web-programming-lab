/*
=========================================================
Experiment 7
Program 4: Check Positive or Negative Number
=========================================================

Aim:
To determine whether a number is positive,
negative, or zero.

Objectives:
• Learn nested conditions.
• Compare numerical values.

Theory:
Numbers greater than zero are positive, numbers
less than zero are negative, and zero is neither
positive nor negative.

=========================================================
*/

let number = -15;

if (number > 0) {
    console.log("Positive Number");
}
else if (number < 0) {
    console.log("Negative Number");
}
else {
    console.log("Number is Zero");
}

/*
Expected Output

Negative Number

Result:
Thus, the program successfully determined the nature
of the given number.
*/
